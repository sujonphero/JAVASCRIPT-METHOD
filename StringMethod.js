// startsWith -- Note: (startsWith) -- mean find in string variable first position an element. 
const starts = "my name is sujon sheikh";
console.log(starts.startsWith("my"))

// --------------------------------------------------------------

// endWith -- Note: (endsWith) -- mean find in string variable last position an element.
const ends = "I love programming";

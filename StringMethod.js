// --------------------------------------------------------------

// includes -- Note: (includes) -- mean find in string variable anywhere position an variable.
const include = "I have no girlfriend";
console.log(include.includes("no"));

// --------------------------------------------------------------

// repeat -- Note: (repeat) mean -- string variable repeat again. Depend according to number command.
const again = "wow i want to job";
console.log(again.repeat(10));